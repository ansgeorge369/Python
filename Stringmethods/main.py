name = "edmond dante"
print(name)
print("The length of the name is", len(name))
print(name.find("d"))  # find the index
print(name.capitalize())  # capitalize the first letter of first word in the string
print(name.upper())  # converts entire string to upper case
print(name.lower())  # converts entire string to lower case
print(name.isdigit())  # checks if the string is digit
print(name.isalpha())  # checks for alphabets possible only without spaces
print(name.count("d"))  # counts a specific character
print(name.replace("o", "a"))  # replaces the character with new character
print(name*5)